package java_5;
     //Kiểu dữ liệu trong java
public class Vidu {
       public static void main(String[] args) {
		 //string là khai báo 1 cái chuỗi
    	  String tenSach = "Lập Trình Java";
		 
    	 //int, short, long là khai báo số nguyên
    	  int namXuatBan = 2021;
		  
    	 //double, float là khai báo số thập phân
    	  double giaBan = 15.5;
		  
    	 //boolean là trạng thái đúng hoặc sai
    	  boolean conSach = true;
    	  
    	 //char là khai báo ký tự
    	  char maKho = 'M';
    	 System.out.println("Tên sách: "+ tenSach);
    	 System.out.println("Năm Xuất Bản: "+namXuatBan);
    	 System.out.println("Giá bán: "+giaBan);
    	 System.out.println("Trạng thái: "+conSach);
    	 System.out.println("Mã kho: "+maKho);
	}
}
